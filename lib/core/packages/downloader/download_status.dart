/// Статус скачивания модели
enum DownloadStatus {
  /// Модель не скачана
  idle,

  /// В очереди на скачивание
  queued,

  /// Скачивается (progress 0.0-1.0)
  downloading,

  /// Распаковывается
  extracting,

  /// Готова к использованию
  completed,

  /// Ошибка при скачивании
  failed,

  /// Отменена пользователем
  cancelled,

  /// Приостановлена
  paused,
}
